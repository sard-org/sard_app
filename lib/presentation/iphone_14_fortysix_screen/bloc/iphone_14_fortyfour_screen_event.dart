part of 'iphone_14_fortysix_bloc.dart';

/// Abstract class for all events that can be dispatched from the
///Iphone14Fortysix widget.
///
/// Events must be immutable and implement the [Equatable] interface.
class Iphone14FortysixEvent extends Equatable {
  @override
  List<Object?> get props => [];
}

/// Event that is dispatched when the Iphone14Fortysix widget is first created.
class Iphone14FortysixInitialEvent extends Iphone14FortysixEvent {
  @override
  List<Object?> get props => [];
}

